export function generateStoryMarkdown(): string {
  return `# Desert Survival Story

## Chapter 1: The Outpost

The sun beat down mercilessly as Alice Chen wiped sweat from her brow, her eyes scanning the crumbling adobe walls of what was once a thriving trading post. The expedition had been in the Sahara for three weeks, following ancient caravan routes and hoping to find evidence of a lost civilization.

"Anything?" Marcus Webb's voice carried across the courtyard, his weathered face squinting against the glare.

"Nothing yet, but the ground-penetrating radar detected anomalies under the central courtyard," Alice replied, gesturing to the ancient stone well at the center of the outpost.

Dr. Fatima Hassan approached, her headscarf fluttering in the hot wind. "I've been studying the inscriptions on these walls. They're Berber, but there's something... off about them. Like they're warning us about something."

Alice felt a chill despite the heat. "Let's dig here. I have a feeling we're close."

## Chapter 2: The Entrance

Two days later, their shovels hit something solid. Not stone, but metal—ancient bronze, intricately worked with symbols none of them recognized.

"This can't be right," Marcus muttered, examining the bronze door. "This style predates anything we've found in this region by at least a thousand years."

As they cleared away the sand, the full extent of the entrance became clear: a massive circular door, ten feet across, set into the bedrock beneath the outpost. The symbols glowed faintly in the afternoon light.

"Alice, look at this." Fatima held up her magnifying glass, revealing a line of script around the door's edge. "It says 'Those who enter forget the sun.'"

## Chapter 3: The Descent

The tomb was vast, far larger than any structure they'd seen from above. Hallways extended in every direction, walls covered in hieroglyphics that seemed to shift in the flickering light of their flashlights.

"These aren't Egyptian, or even Berber," Fatima whispered, her voice echoing. "This is something else entirely."

They moved deeper, finding chambers filled with artifacts: golden statues, pottery, scrolls. But everything felt wrong—too preserved, too perfect, as if untouched by time.

In the central chamber, they found the sarcophagus. It was massive, carved from a single block of black stone, covered in the same strange symbols as the entrance.

## Chapter 4: Awakening

The first tremor came as they were examining the sarcophagus. Dust rained down from the ceiling.

"We need to get out of here," Marcus said, his voice urgent. "This structure isn't stable."

But as they turned to leave, the entrance sealed itself with a grinding of stone on stone. They were trapped.

The tremors continued, growing stronger. Pieces of the ceiling began to fall. In the chaos, Alice saw something move in the shadows—a shape that shouldn't exist, something ancient and wrong.

## Chapter 5: Escape

"The western passage!" Fatima shouted, pointing to a corridor they hadn't explored. "These inscriptions show an alternate route!"

They ran, dodging falling debris, the sound of something massive shifting behind them. The corridor sloped upward, and they could see daylight ahead.

As they emerged into the blinding desert sun, the ground beneath the outpost collapsed, swallowing the tomb beneath the sands once more.

They had escaped. They had found what they came for—but the cost had been too high. One of their team was gone, lost in the collapse.

Alice looked back at the empty space where the outpost had been. Some secrets were meant to stay buried. And as the desert winds began to cover the last traces of their discovery, she realized they would carry the weight of what they'd seen—and what they'd lost—for the rest of their lives.

## Epilogue

Three months later, the artifacts they'd salvaged sat in Alice's lab. Each one was perfect, untouched by time. But she couldn't shake the feeling that they weren't truly hers, that something was waiting in the shadows for her to make a mistake.

The desert had shown them a glimpse of something beyond understanding. And some doors, once opened, could never be fully closed again.

`;
}

